package org.dashbuilder.client.summit.table;

public class Applicant {

    private int age;
    private String country;
    private String name;
    private boolean hasCSSSkill;
    private boolean hasJavaScriptSkill;
    private boolean hasHTMLSkill;
    private boolean hasJavaSkill;
    private boolean hasJBPMSkill;
    private boolean hasDroolsSkill;

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getCountry() {
        return country;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setHasCSSSkill(boolean hasCSSSkill) {
        this.hasCSSSkill = hasCSSSkill;
    }

    public boolean isHasCSSSkill() {
        return hasCSSSkill;
    }

    public void setHasJavaScriptSkill(boolean hasJavaScriptSkill) {
        this.hasJavaScriptSkill = hasJavaScriptSkill;
    }

    public boolean isHasJavaScriptSkill() {
        return hasJavaScriptSkill;
    }

    public void setHasHTMLSkill(boolean hasHTMLSkill) {
        this.hasHTMLSkill = hasHTMLSkill;
    }

    public boolean isHasHTMLSkill() {
        return hasHTMLSkill;
    }

    public void setHasJavaSkill(boolean hasJavaSkill) {
        this.hasJavaSkill = hasJavaSkill;
    }

    public boolean isHasJavaSkill() {
        return hasJavaSkill;
    }

    public void setHasJBPMSkill(boolean hasJBPMSkill) {
        this.hasJBPMSkill = hasJBPMSkill;
    }

    public boolean isHasJBPMSkill() {
        return hasJBPMSkill;
    }

    public void setHasDroolsSkill(boolean hasDroolsSkill) {
        this.hasDroolsSkill = hasDroolsSkill;
    }

    public boolean isHasDroolsSkill() {
        return hasDroolsSkill;
    }
}
